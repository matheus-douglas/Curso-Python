# Tipo None

"""
O tipo de dados None em pyton representa o tipo sem tipo, ou poderia ser conhecido
como tipo vazio, porém, é falar que é um tipo sem tipo é mais apropiado.

Obs: O tipo None é SEMPRE especificado com a primeira letra maiúscula.
    certo: None
    Errado: none

Quando utilizar?

- podemos utilizar None, quando queremos criar uma variável e iniciarlizá-la com um tipo sem tipo,
antes de recebermos um valor final

Obs: O tipo None em python é considerado como sempre False
"""
NUMERO = None
print(NUMERO)
print(type(NUMERO))
